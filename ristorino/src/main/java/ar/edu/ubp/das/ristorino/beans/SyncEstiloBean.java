package ar.edu.ubp.das.ristorino.beans;

public class SyncEstiloBean {
    private int nroEstilo;
    private String nomEstilo;
    private boolean habilitado;

    public int getNroEstilo() { return nroEstilo; }
    public void setNroEstilo(int nroEstilo) { this.nroEstilo = nroEstilo; }
    public String getNomEstilo() { return nomEstilo; }
    public void setNomEstilo(String nomEstilo) { this.nomEstilo = nomEstilo; }
    public boolean isHabilitado() { return habilitado; }
    public void setHabilitado(boolean habilitado) { this.habilitado = habilitado; }
}
